def save_items_to_json(items):
    """保存事物信息到json文件"""
    try:
        with open('kid_car_flutter/assets/car.json', 'w', encoding='utf-8') as f:
            json.dump(items, f, ensure_ascii=False, indent=2)
        return True
    except Exception as e:
        print(f"保存car.json文件失败: {e}")
        return False

